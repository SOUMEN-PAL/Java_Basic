package Learn;

public class Bitwise {

    public static void main(String [] args){
        int x = 3 , y = 6;
        // Bitwise And //
        System.out.println(x&y);
        // Bitwise Or //
        System.out.println(x|y);

        int l = -2;
        System.out.println(l>>2);
        System.out.println(l>>>1);
        
    }
    
}
